const mongoose = require('mongoose');

const FishSchema = new mongoose.Schema({
    id: {
        type: Number,
        required: true,
        unique: true,
    },
    name: {
        type: String,
        required: true,
    },
    category: {
        type: String,
        required: true,
    },
    image: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    price: {
        type: String,
        require: true,
    }
});

const Fish = mongoose.model('Fish', FishSchema);

module.exports = Fish;